              <p class="mbt-5">石蜡切片是组织学常规制片技术中应用最为广泛的方法。但在石蜡切片过程中，经常会遇到一些问题：修整蜡块总修不平整？切片时容易碎片、翻卷？摊片时总是铺不平整？今天和大家分享如何切出符合需求的石蜡切片！</p>
