              </ul>
              <p class="mbt-5 text-indent-2"><b>三、石蜡切片操作要点</b></p>
              <ul class="ul-style-disc mbt-5">
                <li>切片前，把切片机上相关<b>锁杆或螺旋拧紧</b>，否则可能出现跳片、切片厚薄不均现象。</li>
                <li><b>刮净包埋盒周边的余蜡</b>，否则样品夹持可能松动，影响切片质量。</li>
                <li>正确修块，先粗修，厚度大约在15-30微米，质地较硬的组织或较小的组织应再薄一些。粗修至组织全部暴露后，再进行细修。</li>
                <li>选择蜡块冷冻方式，<b>冰盒优于冷冻台</b>。使用冰盒能加水润湿蜡块，冷冻速度快，体积小，使用成本低，无须外接电源。</li>
                <li>切片时轻握手轮，用力均匀轻柔，<b>摇速不宜过快或过慢</b>。过快会导致切片厚薄不均，切片难以展开；过慢会使切片增厚。切片要求完整、薄、均匀。</li>
              </ul>
