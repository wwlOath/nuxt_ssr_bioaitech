              <p class="mbt-5 text-indent-2">一般情况下，<b>标本脱水时间（35℃）为：75%酒精1.5小时、85%酒精2小时、95%酒精（2次）各1.5至2小时、纯酒精（2次）各1.5至2小时。</b>小标本脱水时间与大标本差异不大，一般没必要分开（小标本发脆往往是纸包太厚或相互粘在一起，组织脱水不彻底引起的。如果时间紧急，也可适当缩短脱水时间，脱水时间长短，与室温高低密切相关，同时也与组织的厚薄、组织类型、组织固定的程度等条件有关）。</p>
              <p class="mbt-5 text-indent-2">脱水是否彻底，直接关系到组织是否能充分透明。如果脱水时加温过高（超过50℃），或使用丙酮等，容易导致组织收缩过度和组织发硬发脆。组织脱水引起的组织发脆有三种原因：</p>

              <ul class="ul-style-disc mbt-5">
